// Assignment Operators

x = 10;

x += 5; // same as x = x + 5
x -= 5; // same as x = x - 5
x *= 5; // same as x = x * 5
x /= 5; // same as x = x / 5
x %= 5; // same as x = x % 5
x **= 5; // same as x = x ** 5

// 3. Comparison Operators

x = 2 == 2; // Equal to

x = 2 === 2; // Equal to but equal type as well

x = 2 != 2; // Not equal to

x = 2 !== 2; // Not equal to and qual type

x = 10 > 5; //Greater than

x = 10 < 5; // Less than

x = 10 >= 5; // Greater than or equal to

x = 10 <= 5; // Less than or equal to

